CREATE DATABASE Advising_Checklist;

CREATE TABLE `Adviser` (
  `AdviserID` varchar(20) NOT NULL,
  `A_FirstName` varchar(20) NOT NULL,
  `A_MiddleName` varchar(20) DEFAULT NULL,
  `A_LastName` varchar(20) NOT NULL,
  `AdvisingProgram` varchar(20) DEFAULT NULL
);

CREATE TABLE `Course` (
  `CourseID` varchar(20) NOT NULL,
  `CourseDescription` varchar(100) DEFAULT NULL,
  `Units` tinyint DEFAULT NULL,
  `CourseComponents` varchar(10) DEFAULT NULL,
  `College` varchar(40) DEFAULT NULL,
  `Department` varchar(50) DEFAULT NULL,
  `GradingBasis` varchar(30) DEFAULT NULL,
  `Corequisite` varchar(20) DEFAULT NULL
);

CREATE TABLE `CoursePrerequisite` (
  `CourseID` varchar(20) NOT NULL,
  `Prerequisite` varchar(20) DEFAULT NULL
);

CREATE TABLE `ProgramChecklist` (
  `StudentProgram` varchar(30) NOT NULL,
  `CourseID` varchar(20) DEFAULT NULL,
  `CourseType` text,
  `PrescribedYear` varchar(20) DEFAULT NULL,
  `PrescribedSemester` varchar(20) DEFAULT NULL,
  `DateLastUpdated` date NOT NULL,
  `TimeLastUpdated` time NOT NULL
);

CREATE TABLE `Student` (
  `StudentNumber` varchar(9) NOT NULL,
  `StudentProgram` varchar(30) NOT NULL,
  `AdviserID` varchar(20) DEFAULT NULL,
  `S_FirstName` varchar(30) NOT NULL,
  `S_MiddleName` varchar(30) DEFAULT NULL,
  `S_LastName` varchar(30) NOT NULL,
  `CurrentStanding` varchar(4) DEFAULT NULL,
  `TotalUnitsTaken` int DEFAULT NULL
);

CREATE TABLE `StudentCourseList` (
  `StudentNumber` varchar(9) NOT NULL,
  `CourseID` varchar(20) DEFAULT NULL,
  `CourseStatus` text,
  `Grade` float DEFAULT NULL,
  `StandingTaken` varchar(3) DEFAULT NULL,
  `AcademicYear` varchar(20) DEFAULT NULL,
  `Semester` varchar(10) DEFAULT NULL,
  `DateSubmitted` date DEFAULT NULL,
  `TimeSubmitted` time DEFAULT NULL
);


